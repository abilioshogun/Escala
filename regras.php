<h1>
    Regras da empresa Escala
</h1>

<ol>



    <h2>1 – Obrigatoriedade de uso do uniforme</h2>
    <p>A empresa precisa deixar clara a obrigatoriedade do uso do uniforme, quando houver, explicitando a quantidade de
        peças no enxoval recebido, em um formulário assinado pelo colaborador.</p>
    <p>Caso não haja esta obrigatoriedade, faz-se necessário a elaboração de um código de conduta em relação às
        vestimentas condizentes com o ambiente de trabalho.</p>


    <h2>2 – Instruções para utilização de máquinas e equipamentos</h2>
    <p>Em caso de indústrias, por exemplo, é fundamental que haja um código de instruções sobre o manuseio de máquinas e
        equipamentos, evitando falhas de segurança ou possíveis acidentes de trabalho. </p>
    <p>Neste caso, o colaborador torna-se ciente de como deve ser sua conduta na utilização desses equipamentos.</p>

    <h2>3 – Código de conduta para utilização de computadores e veículos</h2>

    <p>O mesmo equivale para o uso de computador e veículos fornecidos pela empresa. É preciso que o colaborar entenda a
        seriedade e responsabilidade com que deve utilizar tais equipamentos, evitando fazer downloads impróprios, por
        exemplo.</p>
    <p>Ou então, utilizando o veículo da empresa para uso particular (sem prévia autorização) ou infringindo as leis de
        trânsito. O colaborador precisa estar ciente de que não pode denegrir a imagem da empresa e nem descuidar do
        patrimônio que está sob sua responsabilidade.</p>

    <h2>4 – Respeito com superiores e colegas de trabalho</h2>
    <p>Desde o primeiro dia, o colaborar precisa entender a hierarquia da empresa e ter conhecimento de que a empresa
        prima por respeito com os superiores e colegas de trabalho a fim de que o ambiente seja agradável para todos,
        evitando possíveis conflitos e facilitando a gestão de pessoas.</p>

    <h2>5 – Normas sobre faltas e atrasos (e condições para abono dessas faltas)</h2>

    <p>Para evitar conflitos em relação a horários (principalmente em empresas nas quais os funcionários possuem
        horários diferentes), faz-se necessário estabelecer regras em relação às faltas e atrasos, deixando claro as em
        quais condições estas faltas serão abonadas e em quais situações o funcionário precisará compensar as horas
        perdidas.
    </p>

    <h2>6 – Utilização dos benefícios concedidos</h2>
    <p>Empresas que oferecem benefícios como Auxílio Creche, Auxílio Educação ou Gratificações, precisam estabelecer as
        regras gerais que concederão estes benefícios. Assim, ninguém se sentirá lesado.</p>
    <p> Por exemplo, é preciso explicitar que o colaborar só tem direito a Auxílio Educação após um ano de casa ou
        então, qual a porcentagem ou instituições em que este auxílio será concedido.</p>

    <h2>7 – Cultura organizacional da empresa: valores esperados pela empresa em seus funcionários</h2>
    <p>Assim como é fundamental expressar respeito em relação aos superiores e colegas de trabalho, o Código de Conduta
        também serve para demonstrar os valores condizentes com a empresa em questão.</p>
    <p> Ou seja, ética, honestidade e pontualidade, por exemplo. Neste caso, a empresa não tolerará comportamentos que
        não estejam alinhados a essas normas.</p>

    <h2>8 – Punições previstas</h2>
    <p>As punições previstas para em caso de desobediência dos subordinados, faltas leves ou graves, precisam ser
        estipuladas para que não haja falha de comunicação. Desta maneira, quando o funcionário infringir alguma regra
        ou norma, ele saberá que haverá consequência, o que torna o ambiente de trabalho mais organizado e respeitoso.
    </p>
    <h2>9 – Licenças previstas</h2>
    <p>O Regulamento Interno também precisa tratar das licenças previstas em contrato. Por exemplo, as licenças
        previstas em lei: como licença maternidade, licença paternidade, licença por motivos de doença, etc.</p>
    <p> Além disso, a empresa também pode citar licenças que ela concede, como: licença para fazer mestrado, faculdade,
        etc</p>

    <h2>10 – Procedimento para recebimento de salário, férias, etc.</h2>
    <p>No Regulamento Interno, e também no contrato de trabalho, é preciso esclarecer os procedimentos para recebimento
        do salário, descrevendo as formas de pagamento e outros pormenores.</p>
    <p> Além disso, o colaborador precisa estar ciente dos procedimentos e formas para pedido de concessão de férias,
        por exemplo.</p>

    <p>Como você pôde perceber, é fundamental que a empresa relacione todos os assuntos de grande importância de forma
        clara e objetiva, tendo certeza de que o colaborador está ciente de todas as normas e regras vigentes e também
        de que o Regulamento Interno respeita a legislação trabalhista e disposições sindicais.</p>

